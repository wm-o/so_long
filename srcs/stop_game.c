#include "so_long.h"

void	game_over(t_game *g, t_queue *q)
{
	write(1, "game over\n", 10);
	if (q)
	{
		while (q->size)
			dequeue(q);
	}
	free_map(g);
	free_mlx(g);
	exit(0);
}
